                                    <h4><a href="single-blog.html">La poussée dentaire</a></h4>
                                    <span>By : <a href="#">Dre Thao Phan, dentiste pédiatrique</a> | <a href="#">210 Like</a> | <a href="#">69 Comments</a></span>
                                    h3>Les symptômes</h3>
                                                <p><ul>Votre enfant : 
                                                    <li>- salive beaucoup et a tendance à mordiller;</li>
                                                    <li>- est irritable et pleure facilement;</li>
                                                    <li>- a les joues rouges;</li>
                                                    <li>- mange et dort moins bien que d’habitude;</li>
                                                    <li>- a les gencives un peu gonflées;</li>
                                                    <li>- a une petite bulle bleue sur la gencive (un « kyste d’éruption »)
                                                        <br>annonçant la percée prochaine d’une dent. </li>
                                                    </ul></p>
                                                    <strong>Normalement, les poussées dentaires ne provoquent pas de complications.
                                                        <br>Si votre enfant présente d’autres symptômes, il souffre probablement d’autre chose.
                                                        <br>En cas de fièvre élevée, la poussée dentaire est rarement en cause.</strong>
                                                    <br>
                                                    <br>
                                                    <h3>Qu’est-ce que les poussées dentaires?</h3>
                                                    <p>Chez certains enfants, l’apparition des dents de lait (aussi appelées « dents primaires » ou « dents de bébé »)
                                                        <br>passe presque inaperçue, tandis que chez d’autres elle s’accompagne de malaises ou d’inconforts. </p>
                                                        <p><strong>Âge des premières dents : </strong>
                                                        <br>Les premières dents percent généralement vers l’âge de 6 mois.
                                                        <br>Elles peuvent toutefois apparaître plus tôt. Certains enfants naissent même avec une ou deux dents.
                                                        <br>Dans d’autres cas, les premières dents ne sortent qu’à l’âge de 14 mois.</p>
                                                        <p><ul>L’âge d’apparition des dents est très variable. En général, elles sortent toutefois dans l’ordre suivant :
                                                            <li>- les deux incisives du bas, puis celles du haut (entre 6 et 12 mois); </li>
                                                            <li>- les incisives latérales (entre 9 et 13 mois);</li>
                                                            <li>- les premières molaires (entre 13 et 19 mois);</li>
                                                            <li>- les canines (entre 16 et 22 mois);</li>
                                                            <li>- enfin, les deuxièmes molaires (entre 25 et 33 mois).</li>
                                                        </ul></p>
                                                        <strong>Combien de dents de bébé en tout?</strong>
                                                        <p>Dans la majorité des cas, à 3 ans, l’enfant a ses 20 dents primaires. </p>
                                                        <p>Pendant ce temps, les 32 dents d’adulte se développent dans les mâchoires. Elles apparaissent entre 6 et 12 ans environ.
                                                            <br>Quant aux dents de sagesse, elles percent généralement plus tard. Certaines personnes n’ont toutefois pas de dents de sagesse.</p>
                                                        <br>
                                                        <h3>Comment traiter?</h3>
                                                        <h3>Soins et conseils pratiques</h3>
                                                        <p>Les poussées de dents ne nécessitent pas de traitements. Il n’y a rien à faire à part soulager l’inconfort.</p>
                                                        <p>N’hésitez surtout pas à réconforter et à cajoler votre enfant quand une de ses dents perce.
                                                            <br>Les quelques soins qui suivent vous aideront à soulager son malaise ou son inconfort. </p>
                                                        <p><ul>
                                                            <li>- <strong>Frottez-lui doucement les gencives</strong> avec une débarbouillette imbibée d’eau froide enroulée autour de votre doigt.
                                                                <br>Assurez-vous toutefois de bien laver vos mains d’abord.
                                                                <br>Vous pouvez laisser votre bébé mâchouiller la débarbouillette sous votre supervision.
                                                                <br>Vous pouvez aussi lui masser les gencives très doucement avec une brosse à dents pour bébé aux soies très souples.
                                                                <br>Utilisez-la avec beaucoup de délicatesse. </li>
                                                                <br>
                                                                <li>- <strong>Donnez-lui un anneau de dentition en caoutchouc.</strong>Choisissez-en un qui est incassable, qui ne s’effrite pas 
                                                                    <br>et qui est assez gros pour ne pas pouvoir pénétrer entièrement dans la bouche de votre bébé.
                                                                    <br>Idéalement, sa surface doit être texturée.
                                                                    <br>Évitez les anneaux en PVC ou ceux qui renferment du liquide.
                                                                    <br>On peut réfrigérer un anneau de dentition, mais on ne doit pas le congeler.
                                                                    <br>Il ne faut pas non plus l’attacher avec un ruban autour du cou de l’enfant. </li>
                                                                <br>
                                                                <li>- <strong>Au besoin, vous pouvez donner de l’acétaminophène</strong> à votre bébé. </li>
                                                        </ul></p>
                                                        
                                                        <div class="apparte">
                                                        <p><ul><strong>À éviter : le collier de noisetier, les légumes crus et le biscuit de dentition</strong>
                                                            <li>- S’il est très à la mode dans certains milieux, le collier de noisetier est déconseillé par les autorités médicales, car le bébé risque de s’étouffer si des billes se détachent.</li>
                                                            <li>- Ne donnez pas non plus à votre enfant des morceaux de légumes ou de fruits crus « durs » (pomme, carotte, céleri, etc.) avant l’âge de 2 ans afin de prévenir les risques d’étouffement. </li>
                                                            <li>- Les biscuits de dentition sont également déconseillés en raison du sucre qu’ils contiennent qui cause la carie dentaire.</li>
                                                            <li>- Les gels analgésiques topiques sont dangereux, car ils agissent de façon superficielle et risquent de nuire à la capacité du bébé d’avaler.</li>    
                                                        </ul></p>
