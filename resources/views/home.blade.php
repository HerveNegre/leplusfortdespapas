                                    <h3>Les coliques (pleurs intenses)</h3>
                                    <ul class="clearfix">
                                        <li><i class="pe-7s-user"></i>By :<a href="#">Valérie de Beaumont, infirmière bachelière et IBCLC</a><span>|</span></li>
                                        <li><i class="pe-7s-comment"></i><a href="#">Avril 2017 </a><span>|</span></li>
                                        <li><i class="pe-7s-like"></i><a href="#">210 Like</a><span>|</span></li>
                                        <li><i class="pe-7s-back"></i><a href="#">69 Comments</a></li>
                                    </ul>
                                </div>
                                <p> <strong>Q’est-ce que les coliques?</strong></p>
                                <div class="italic">
                                    <p>En général, les coliques sont décrites comme des crises de pleurs récurrentes et régulières chez un bébé. Elles ont lieu au moins 3 heures par jour, 3 jours par semaine, pendant plus d’une semaine. Les crises surviennent en général aux mêmes moments de la journée, le plus souvent en fin d’après-midi ou en soirée. 
                                        <br>Les coliques sont assez fréquentes chez les jeunes bébés : jusqu’à 20 % des nourrissons seraient touchés. Selon certaines études, les bébés allaités exclusivement seraient moins touchés que les bébés nourris avec des préparations commerciales à base de lait de vache.
                                        <br>Les coliques peuvent commencer 3 semaines après la naissance, mais surviennent plus souvent entre la 6e et la 8e semaine. Elles disparaissent graduellement vers le 4e mois.
                                        <br></p>
                                </div>
                                <p><ul>Lorsqu’un bébé fait des coliques, les pleurs peuvent être accompagnés de certains comportements caractéristiques. Le bébé :
                                    <li>- serre les poings et devient rouge;</li>
                                    <li>- raidit les jambes et les muscles de son ventre. Il peut aussi replier les jambes contre son ventre;</li>
                                    <li>- a le ventre dur et a souvent des gaz pendant une crise;</li>
                                    <li>- peut aussi arquer le dos.</li>
                                </ul></p>
                                <div class="apparte">
                                    <p>Les coliques ne sont pas une maladie, mais plutôt la description d’un comportement.<br>
                                        Les seuls et uniques réels symptômes toujours associés aux coliques sont d’ailleurs les pleurs intenses et soutenus.</p>
                                    </div><br><br>
                                    <p>Par ailleurs, un bébé qui a des coliques est un enfant en bonne santé. Il a un réflexe de succion normal. Il boit bien. Il peut régurgiter à l’occasion, mais ne vomit pas. Il ne fait pas de fièvre. Il n’est pas léthargique. Ses selles sont normales et il mouille 6 à 8 couches par jour. De plus, il a toujours un gain de poids satisfaisant, c’est-à-dire qu’il suit sa courbe de croissance. </p>
                                    <br>
                                    <h3>Quelles sont les causes des coliques?</h3>
                                    <p>La cause des coliques demeure souvent un mystère.
                                        <br>Elle ne peut être clairement déterminée que dans 5 % des cas. Plusieurs hypothèses existent toutefois pour tenter d’en expliquer l’origine.</p>
                                    <br>
                                    <h3>Comment soulager les coliques de bébé?</h3>
                                    <p><ul>
                                        <li>Garder l’environnement très calme autour du bébé et baisser les lumières;</li>
                                        <li>Bercer le bébé doucement;</li>
                                        <li>Porter le bébé dans un porte-bébé ou une écharpe;</li>
                                        <li>Faire un massage abdominal en frottant dans le sens des aiguilles d’une montre;</li>
                                        <li>Éviter la suralimentation (trop de biberons ou de tétées);</li>
                                        <li>Limiter l’absorption d’air durant la tétée ou le biberon et essayer de bien faire faire le rot;</li>
                                        <li>Pour les bébés allaités, ne donner qu’un seul sein par tétée ou le même sein pour 2 tétées de suite de façon à ce que bébé atteigne le lait gras de fin de tétée qui peut prévenir les coliques. </li>
                                    </ul></p>
                                    <div class="apparte">
                                        <p>
                                            Dans une telle situation, il est normal de ressentir de la colère. <br>La colère se manifeste en effet dans des situations imprévisibles et incontrôlables. <br>Cette émotion n’a rien à voir avec le fait d’être un mauvais parent. <br>Il est toutefois important de ne pas s’emporter. 
                                        </p>
